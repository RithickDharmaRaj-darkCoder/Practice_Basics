# String Methods...
print("Normal String")
a = "RITHICK DHARMA RAJ"
print(a,'\n')

print("capatalize()")
b = a.capitalize()
print(b,'\n')

print("casefold()")
b = a.casefold()
print(b,'\n')

print("center()")
b = a.center(30,"-")
print(b,'\n')

print("count()")
b = a.count('A')
print(b,'\n')

print("encode()")
b = a.encode()
print(b,'\n')

print("endswith()")
b = a.endswith("J")
print(b)
